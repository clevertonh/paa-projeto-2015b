    public void fNovoRegistro()
    {
        //
        this.objProducaoLeite = new ProducaoLeite();
        //Por padrão oculta o botão excluir
        btnExcluir.setEnabled(false);
        //Por padrão limpa o campo texto
        limparCampos();
        //Adiciona o status de edição
        this.edicao = false;
        //Seta o focu no campo de descrição ao abrir o formulário
        btnPesqAnimal.requestFocus();
    }

    public void fCarregaCadastro(int id)
    {
        //Se for passado um código por parâmetro, pesquisa no banco
        this.objProducaoLeite = this.objController.findById(id);
        //Carrega na tela o nome
        txtIdAnimal.setText(this.objProducaoLeite.getAnimal().getId().toString());
        txtNomeAnimal.setText(this.objProducaoLeite.getAnimal().getNome());

        txtDataRegistro.setText("");
        //Carrega na tela a quantidade
        txtQuantidade.setText(this.objProducaoLeite.getQuantidadeMl().toString());
        //Adiciona o status de edição
        this.edicao = true;
        //MOstra o botão excluir
        btnExcluir.setEnabled(true);
        //Seta o focu no campo de descrição ao abrir o formulário
        btnPesqAnimal.requestFocus();
    }

    public void fPesquisaAnimal()
    {
        if (frmPesqAnimal == null) {
            frmPesqAnimal = new FrmPesquisarAnimal();
            frmPesqAnimal.setModal(true);
        }

        if (frmPesqAnimal.isVisible()) {
            frmPesqAnimal.setVisible(false);
        } else {
            frmPesqAnimal.setLocationRelativeTo(null);
            frmPesqAnimal.setVisible(true);
        }

    }

