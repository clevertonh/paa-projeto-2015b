    public void fAbreCadastro(int id)
    {
        if (frmAnimal == null) {
            frmAnimal = new FrmAnimalCadastro();
            frmAnimal.setModal(true);
        }

        if (id > 0) {
            frmAnimal.fCarregaCadastro(id);
        }

        if (frmAnimal.isVisible()) {
            frmAnimal.setVisible(false);
        } else {
            frmAnimal.setLocationRelativeTo(null);
            frmAnimal.setVisible(true);
        }

        pesquisar();
    }

